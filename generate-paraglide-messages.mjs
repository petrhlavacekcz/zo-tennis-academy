#!/usr/bin/env node
// Generates src/lib/paraglide/messages/_index.js from the messages/*.json files
// This is a workaround for network-restricted environments where Paraglide can't
// fetch its CDN plugins during the build.

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const locales = ['cs', 'en', 'de', 'pl'];
const baseLocale = 'cs';

// Load all message files
const messages = {};
for (const locale of locales) {
  const filePath = join(__dirname, 'messages', `${locale}.json`);
  const raw = JSON.parse(readFileSync(filePath, 'utf-8'));
  // Remove $schema key
  delete raw['$schema'];
  messages[locale] = raw;
}

// Flatten nested object to dot-notation keys
function flatten(obj, prefix = '') {
  const result = {};
  for (const [key, value] of Object.entries(obj)) {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
      Object.assign(result, flatten(value, fullKey));
    } else {
      result[fullKey] = value;
    }
  }
  return result;
}

// Flatten all locales
const flat = {};
for (const locale of locales) {
  flat[locale] = flatten(messages[locale]);
}

// Collect all keys (from base locale)
const allKeys = Object.keys(flat[baseLocale]);

// Sanitize key to valid JS identifier
function toIdentifier(key) {
  return '_msg_' + key.replace(/[^a-zA-Z0-9]/g, '_');
}

// Escape a string for JS string literal
function escapeStr(s) {
  if (typeof s !== 'string') return String(s);
  return s
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

// Generate the _index.js content
let output = `/* eslint-disable */
/* Auto-generated by generate-paraglide-messages.mjs */
import { getLocale } from '../runtime.js';

`;

const exportPairs = [];

for (const key of allKeys) {
  const fnName = toIdentifier(key);
  const csVal = flat['cs'][key] ?? '';
  const enVal = flat['en']?.[key] ?? csVal;
  const deVal = flat['de']?.[key] ?? csVal;
  const plVal = flat['pl']?.[key] ?? csVal;

  output += `function ${fnName}() {\n`;
  output += `  const locale = getLocale();\n`;
  output += `  if (locale === "en") return \`${escapeStr(enVal)}\`;\n`;
  output += `  if (locale === "de") return \`${escapeStr(deVal)}\`;\n`;
  output += `  if (locale === "pl") return \`${escapeStr(plVal)}\`;\n`;
  output += `  return \`${escapeStr(csVal)}\`;\n`;
  output += `}\n`;

  exportPairs.push(`  ${fnName} as "${key}"`);
}

output += `\nexport {\n${exportPairs.join(',\n')}\n};\n`;

const outDir = join(__dirname, 'src', 'lib', 'paraglide', 'messages');
mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, '_index.js');
writeFileSync(outFile, output, 'utf-8');
console.log(`✓ Generated ${allKeys.length} message functions → ${outFile}`);
